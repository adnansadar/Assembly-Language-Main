.MODEL SMALL

PROMPT MACRO MSG
MOV AH,09H
LEA DX,MSG
INT 21H
ENDM

.DATA
MSG1 DB 10,13,"ENTER 5 ELEMENTS IN TO SOURCE BLOCK:$"
MSG2 DB 10,13,"BLOCK BEFORE OVERLAPPING IS:$"    
MSG3 DB 10,13,"ENTER POSITION OF OVERLAP:$"
MSG4 DB 10,13,"THE BLOCK AFTER OVERLAPING:$" 

SRC DB 10 DUP(0)
POS DB 00


.CODE

MOV AX,@DATA
MOV DS,AX


PROMPT MSG1

LEA SI,SRC
MOV CL,05

ACC:
CALL ACCEPT
MOV [SI],BL
INC SI
DEC CL
JNZ ACC

PROMPT MSG2
LEA SI,SRC 
MOV CL,05 

MV:
MOV BL,[SI] 
CALL DISP

INC SI
DEC CL
JNZ MV 



LEA SI,SRC 
MOV CL,05 
MOV CH,00

ADD SI,CX
DEC SI
MOV DI,SI

PROMPT MSG3
CALL ACCEPT 

MOV POS,BL

MOV BH,00

ADD DI,BX
DEC DI

MOV CL,05

TR1:
MOV BL,[SI]
MOV [DI],BL

DEC SI
DEC DI
DEC CL

JNZ TR1 

PROMPT MSG4

MOV CL,05
ADD CL,POS 
DEC CL



LEA SI,SRC

DSP1:
MOV BL,[SI]
CALL DISP  

INC SI
DEC CL
JNZ DSP1

MOV AH,4CH
INT 21H

ACCEPT PROC

MOV AH,01H
INT 21H

CMP AL,39H
JBE X1
SUB AL,07H
X1:SUB AL,30H

MOV BL,AL
ROL BL,04H

MOV AH,01H
INT 21H

CMP AL,39H
JBE X2
SUB AL,07H
X2:SUB AL,30H

ADD BL,AL 

RET
ENDP



 
DISP PROC
MOV DL,BL
ROL DL,04H
AND DL,0FH

CMP DL,09H
JBE X3
ADD DL,07H
X3:ADD DL,30H

MOV AH,02H
INT 21H   

MOV DL,BL
AND DL,0FH

CMP DL,09H
JBE X4
ADD DL,07H
X4:ADD DL,30H

MOV AH,02H
INT 21H  

RET
ENDP

END












