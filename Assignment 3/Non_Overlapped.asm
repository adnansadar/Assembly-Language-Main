

.MODEL SMALL

PROMPT MACRO MSG
MOV AH,09H
LEA DX,MSG
INT 21H
ENDM 

PROMPT1 MACRO
MOV DL,','
MOV AH,02H
INT 21H
ENDM 

.DATA
MSG1 DB 10,13,"ENTER 5 ELEMENTS IN TO SOURCE BLOCK:$"
MSG2 DB 10,13,"SOURCE BLOCK IS:$"
MSG3 DB 10,13,"DESTINATION BLOCK IS:$"    
MSG4 DB ",$"

SRC DB 05 DUP(0)
DST DB 05 DUP(0)

.CODE

MOV AX,@DATA
MOV DS,AX


PROMPT MSG1

LEA SI,SRC
MOV CL,05

ACC:
CALL ACCEPT
MOV [SI],BL
INC SI
DEC CL
JNZ ACC

LEA SI,SRC
LEA DI,DST
MOV CL,05 

MV:
MOV BL,[SI] 
MOV [DI],BL
;CALL DISP


INC SI
INC DI
DEC CL
JNZ MV

PROMPT MSG2

LEA SI,SRC
MOV CL,05

DSP1:
MOV BL,[SI]  
CALL DISP
INC SI
DEC CL
JNZ DSP1

PROMPT MSG3 

LEA SI,DST
MOV CL,05

DSP2:
MOV BL,[SI] 
CALL DISP 
PROMPT1
INC SI
DEC CL
JNZ DSP2

MOV AH,4CH
INT 21H

ACCEPT PROC

MOV AH,01H
INT 21H

CMP AL,39H
JBE X1
SUB AL,07H
X1:SUB AL,30H

MOV BL,AL
ROL BL,04H

MOV AH,01H
INT 21H

CMP AL,39H
JBE X2
SUB AL,07H
X2:SUB AL,30H

ADD BL,AL 

RET
ENDP



 
DISP PROC
MOV DL,BL
ROL DL,04H
AND DL,0FH

CMP DL,09H
JBE X3
ADD DL,07H
X3:ADD DL,30H

MOV AH,02H
INT 21H   

MOV DL,BL
AND DL,0FH

CMP DL,09H
JBE X4
ADD DL,07H
X4:ADD DL,30H

MOV AH,02H
INT 21H  

RET
ENDP

END









